            return view('dashboard', [
                'mahasiswas' => Mahasiswa::sortable()->where('nama', 'LIKE', '%' .$request->search. '%')->orWhere('prodi', 'LIKE', '%' .$request->search. '%')->paginate(5),
            ]);
        } else {
            return view('dashboard', [
